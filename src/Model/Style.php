<?php

namespace Model;

/**
 * Class Style
 * @package Model
 */
class Style{
    /**
     * @var
     */
    private $id;
    /**
     * @var
     */
    private $name;

	/**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

}